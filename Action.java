public class Action {
    String addition(int num1, int num2) {
        return "Addition of " + num1 + " & " + num2 + " is : " + (num1 + num2);
    }

    String substraction(int num1, int num2) {
        return "Substraction of " + num1 + " & " + num2 + " is : " + (num1 - num2);
    }

    String multiply(int num1, int num2) {
        return "Multiplication of " + num1 + " & " + num2 + " is : " + (num1 * num2);
    }

    String division(int num1, int num2) {
        return "Division of " + num1 + " & " + num2 + " is : " + (num1 / num2);
    }
}
